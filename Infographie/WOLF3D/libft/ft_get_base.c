#include "libft.h"

char	*ft_get_base(int n)
{
	int		i;
	char	*s;

	i = ~(!n);
	s = ft_strnew(n + 1);
	while (++i < n)
		*(s + i) = (i < 10) ? ('0' + i) : ('A' + (i - 10));
	return (s);
}
