#include "libmlxft.h"

int			ft_get_color(int rgb, int opt)
{
	return ((rgb >> opt) & 0xFF);
}

int			ft_get_rgb(int r, int g, int b)
{
	return (((r & 0xFF) << 0x10) | ((g & 0xFF) << 0x08) | (b & 0xFF));
}
