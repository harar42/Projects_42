#include "libftx.h"

char	ftx_strcmp_sh(const char *s1, const char *s2, char equ)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i])
		i++;
	if (equ)
		return (s1[i] == '=' && s2[i] == '\0');
	return ((s1[i] == s2[i] || s1[i] == ' ' || s1[i] == '\t') && s2[i] == '\0');
}
