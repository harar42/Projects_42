#include "libft.h"

void		ft_putendl(char const *s)
{
	int		i;
	char	*ptrs;

	i = 0;
	ptrs = (char*)s;
	while (ptrs[i] != '\0')
		ft_putchar(ptrs[i++]);
	ft_putchar('\n');
}
