	global	_ft_isascii

_ft_isascii:
	cmp		rdi, 0x0
	jl		Ret
	cmp		rdi, 0x7f
	jg		Ret
	mov		rax, 0x1
	ret

Ret:
	xor		rax, rax
	ret
