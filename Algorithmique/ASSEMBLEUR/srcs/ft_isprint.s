	global	_ft_isprint

_ft_isprint:
	cmp		rdi, 0x20
	jl		Ret
	cmp		rdi, 0x7e
	jg		Ret
	mov		rax, 0x1
	ret

Ret:
	xor		rax, rax
	ret
